def collate_string(
    string,
    src_pipeline,
    src_vocab,
    device,
    max_padding=128,
    pad_id=2,
):
    bs_id = torch.tensor([0], device=device)  # <s> token id
    eos_id = torch.tensor([1], device=device)  # </s> token id
    src_list = []
    processed_src = torch.cat(
        [
            bs_id,
            torch.tensor(
                src_vocab([src_pipeline(string)]),
                dtype=torch.int64,
                device=device,
            ),
            eos_id,
        ],
        0,
    )
    src_list.append(
        # warning - overwrites values for negative values of padding - len
        pad(
            processed_src,
            (
                0, # Padding Left
                max_padding - len(string), # Padding Right
            ),
            value=pad_id,
        )
    )

    # Concatenates sequence of tensors along a new dimension
    src = torch.stack(src_list) 
    return src

